#!/usr/bin/env node
/* ════════════════════════════════════════════════════════════════
   generate-sections.js — section manifest generator
   ----------------------------------------------------------------
   Walks the built /prime/ pages, extracts each page's <h2> headings
   (id + label), and writes /scripts/dm-sections.js as a static
   manifest the sidebar reads to render expandable, cross-page
   section navigation.

   Labeling:
     - If an <h2> has a data-nav-label="..." attribute, that text is
       used as the sidebar label (for shortening long headings).
     - Otherwise the heading's text content is used, with any inner
       tags stripped and whitespace collapsed.

   Only first-level (h2) sections are listed — h3s are intentionally
   excluded to keep the sidebar shallow.

   Run from the repo root (the dir containing /prime and /scripts):
       node scripts/generate-sections.js

   Re-run whenever you add, remove, rename, or reorder h2 sections.
   The manifest is DERIVED from the HTML, so it never drifts as long
   as you regenerate after structural edits.
   ════════════════════════════════════════════════════════════════ */

'use strict';

const fs = require('fs');
const path = require('path');

// Resolve repo root relative to this script (scripts/ -> ..).
const REPO_ROOT = path.resolve(__dirname, '..');
const PRIME_DIR = path.join(REPO_ROOT, 'prime');
const OUT_FILE = path.join(REPO_ROOT, 'scripts', 'dm-sections.js');

// Pages to scan. Keys are the page paths RELATIVE TO /prime/ — these
// are exactly the strings the sidebar uses to match the current page.
// Subdir pages (voyages/index.html) are included with their subpath.
function collectPages() {
  const pages = [];

  // Top-level /prime/*.html
  fs.readdirSync(PRIME_DIR).forEach((entry) => {
    const full = path.join(PRIME_DIR, entry);
    if (fs.statSync(full).isFile() && entry.endsWith('.html')) {
      pages.push({ key: entry, file: full });
    }
  });

  // One level of subdirectories (e.g. voyages/).
  fs.readdirSync(PRIME_DIR).forEach((entry) => {
    const full = path.join(PRIME_DIR, entry);
    if (fs.statSync(full).isDirectory()) {
      fs.readdirSync(full).forEach((sub) => {
        if (sub.endsWith('.html')) {
          pages.push({ key: entry + '/' + sub, file: path.join(full, sub) });
        }
      });
    }
  });

  return pages;
}

// Extract h2 id + label from a page's HTML.
function extractH2s(html) {
  const out = [];
  // Match each <h2 ...>...</h2> block, capturing attributes and inner text.
  const re = /<h2\b([^>]*)>([\s\S]*?)<\/h2>/gi;
  let m;
  while ((m = re.exec(html)) !== null) {
    const attrs = m[1];
    const inner = m[2];

    const idMatch = attrs.match(/\bid="([^"]+)"/);
    if (!idMatch) continue; // sections without ids can't be linked
    const id = idMatch[1];

    // Skip headings explicitly opted out.
    if (/\bclass="[^"]*\bdm-nav-skip\b[^"]*"/.test(attrs)) continue;

    let label;
    const navLabel = attrs.match(/\bdata-nav-label="([^"]+)"/);
    if (navLabel) {
      label = navLabel[1];
    } else {
      // Remove any status-tag spans entirely (text and all) so their
      // contents don't leak into the label, then strip remaining tags.
      label = inner
        .replace(/<span\b[^>]*\bclass="[^"]*\bdm-tag\b[^"]*"[^>]*>[\s\S]*?<\/span>/gi, '')
        .replace(/<[^>]+>/g, '')
        .replace(/&amp;/g, '&')
        .replace(/&mdash;/g, '—')
        .replace(/\s+/g, ' ')
        .trim();
      // Default shortening: if there's an em-dash clause, keep the head.
      const dash = label.indexOf(' — ');
      if (dash > 0) label = label.slice(0, dash).trim();
    }

    out.push({ id, label });
  }
  return out;
}

function main() {
  const pages = collectPages();
  const manifest = {};

  pages.forEach(({ key, file }) => {
    const html = fs.readFileSync(file, 'utf8');
    const h2s = extractH2s(html);
    if (h2s.length) manifest[key] = h2s;
  });

  const banner =
    '/* AUTO-GENERATED by scripts/generate-sections.js — do not edit by hand.\n' +
    '   Regenerate after adding/removing/reordering h2 sections:\n' +
    '       node scripts/generate-sections.js\n' +
    '   Maps each /prime/ page to its first-level (h2) sections for the\n' +
    '   sidebar\'s expandable cross-page navigation. */\n';

  const body =
    banner +
    'window.DM_SECTIONS = ' +
    JSON.stringify(manifest, null, 2) +
    ';\n';

  fs.writeFileSync(OUT_FILE, body, 'utf8');
  const count = Object.values(manifest).reduce((n, a) => n + a.length, 0);
  console.log(
    'Wrote ' + OUT_FILE + ' — ' +
    Object.keys(manifest).length + ' pages, ' + count + ' sections.'
  );
}

main();
